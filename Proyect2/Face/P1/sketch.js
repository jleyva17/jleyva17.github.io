let title;
let width;
let height;
let subs;

function setup() {
  width = windowWidth;
  height = windowHeight;
  createCanvas(width, height);
  title = "The Founder Society";
  subs = "To Preserve"
}

function draw() {
  background(20);
  textFont("Tahoma");
  textSize(30);
  fill(150);
  text(title, width/2.4 , 100);
  text(subs, width/2.3, height - 200);
  ellipse(200, 200, 20);
}
